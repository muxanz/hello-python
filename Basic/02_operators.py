# Arithmetic operators

print(5 + 3)
print(5 - 3)
print(5 * 3)
print(5 / 3)
print(5 % 3)
print(5 // 3)
print(5 ** 3)

print("Hello" + "python")
print("Python " * 5)
print("Hello " * (2 ** 3))
print("Hello " + str(10))

my_float = 2.5 * 2
